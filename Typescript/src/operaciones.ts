
export class operaciones{
   

    constructor(){
       
    }

    dividir(numero1: number, numero2: number){
        return (numero1 / numero2);
    };

    multiplicar(numero1: number, numero2: number){
        return (numero1 * numero2);
    };

    sumar(numero1: number, numero2: number){
        return (numero1 + numero2);
    };

    restar(numero1: number, numero2: number){
        return (numero1 - numero2);
    };
}